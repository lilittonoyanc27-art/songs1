const CHORUS_THEMES = [
  'bg-indigo-50 border-indigo-200 text-indigo-900',
  'bg-rose-50 border-rose-200 text-rose-900',
  'bg-amber-50 border-amber-200 text-amber-900',
  'bg-emerald-50 border-emerald-200 text-emerald-900',
  'bg-sky-50 border-sky-200 text-sky-900',
  'bg-violet-50 border-violet-200 text-violet-900',
  'bg-orange-50 border-orange-200 text-orange-900',
  'bg-cyan-50 border-cyan-200 text-cyan-950',
  'bg-lime-50 border-lime-200 text-lime-950',
  'bg-fuchsia-50 border-fuchsia-200 text-fuchsia-950',
  'bg-teal-50 border-teal-200 text-teal-950',
  'bg-pink-50 border-pink-200 text-pink-950',
];

export default function App() {
  const [selectedId, setSelectedId] = useState<string>(SONGS[0].id);
  const [isMenuOpen, setIsMenuOpen] = useState(false);

  const selectedSong = SONGS.find(s => s.id === selectedId) || SONGS[0];

  useEffect(() => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  }, [selectedId]);

  const renderLyrics = (lyrics: string, songId: string) => {
    const lines = lyrics.split('\n');
    let themeIndex = parseInt(songId) % CHORUS_THEMES.length;
    if (isNaN(themeIndex)) themeIndex = 0;
    const theme = CHORUS_THEMES[themeIndex];

    const blocks: ReactNode[] = [];
    let currentBlock: string[] = [];
    let isChorus = false;

    lines.forEach((line, index) => {
      // More robust check for "Припев" at the start of a trimmed line
      const lineTrimmed = line.trim().toLocaleLowerCase();
      const isStartOfChorus = lineTrimmed.startsWith('припев') || lineTrimmed.includes('припев:');
      
      if (isStartOfChorus) {
        // Push previous block
        if (currentBlock.length > 0) {
          blocks.push(
            <div key={`block-${index}`} className="mb-8">
              {currentBlock.join('\n')}
            </div>
          );
          currentBlock = [];
        }
        isChorus = true;
      } else if (line.trim() === '' && isChorus) {
        // End of chorus block on empty line
        if (currentBlock.length > 0) {
          blocks.push(
            <div key={`chorus-${index}`} className={`my-12 p-8 md:p-12 rounded-3xl border-2 shadow-sm ${theme} italic font-medium`}>
              <div className="text-xs uppercase tracking-widest font-bold mb-4 opacity-60">Припев</div>
              {currentBlock.join('\n')}
            </div>
          );
          currentBlock = [];
        }
        isChorus = false;
      }
      
      currentBlock.push(line);
    });

    // Push final block
    if (currentBlock.length > 0) {
      if (isChorus) {
        blocks.push(
          <div key="final-chorus" className={`my-12 p-8 md:p-12 rounded-3xl border-2 shadow-sm ${theme} italic font-medium`}>
            <div className="text-xs uppercase tracking-widest font-bold mb-4 opacity-60">Припев</div>
            {currentBlock.join('\n')}
          </div>
        );
      } else {
        blocks.push(
          <div key="final-block" className="mb-8">
            {currentBlock.join('\n')}
          </div>
        );
      }
    }

    return blocks;
  };

  return (
    <div 
      className="min-h-screen bg-neutral-50 text-neutral-900 font-sans p-4 md:p-8"
      style={{ fontFamily: 'Arial, Helvetica, sans-serif' }}
    >
      {/* Mobile Header/Menu Toggle */}
      <div className="fixed top-0 left-0 right-0 bg-white border-b border-neutral-200 z-50 flex items-center justify-between px-6 py-4 md:hidden">
        <div className="flex items-center gap-2">
          <Music className="w-6 h-6 text-indigo-600" />
          <span className="font-bold text-lg">Сборник песен</span>
        </div>
        <button 
          onClick={() => setIsMenuOpen(!isMenuOpen)}
          className="p-2 hover:bg-neutral-100 rounded-lg transition-colors"
          id="menu-toggle"
        >
          {isMenuOpen ? <X className="w-6 h-6" /> : <Menu className="w-6 h-6" />}
        </button>
      </div>

      {/* Desktop Sidebar Navigation */}
      <aside className="hidden md:block fixed left-0 top-0 bottom-0 w-80 bg-white border-r border-neutral-200 p-8 overflow-y-auto">
        <div className="flex items-center gap-3 mb-12">
          <Music className="w-8 h-8 text-indigo-600" />
          <h1 className="text-2xl font-bold tracking-tight">Музыкальный Сборник</h1>
        </div>
        
        <nav className="space-y-2">
          <p className="text-[10px] uppercase tracking-widest text-neutral-400 font-bold mb-4 px-2">Выберите песню ({SONGS.length})</p>
          {SONGS.map((song) => (
            <button
              key={song.id}
              onClick={() => setSelectedId(song.id)}
              id={`nav-desktop-${song.id}`}
              className={`w-full text-left px-4 py-3 rounded-xl transition-all duration-200 ${
                selectedId === song.id 
                ? 'bg-indigo-600 text-white shadow-lg shadow-indigo-200 scale-[1.02]' 
                : 'hover:bg-neutral-100 text-neutral-600'
              }`}
            >
              <div className="font-bold text-lg truncate">
                <span className="opacity-50 mr-2 text-sm">{song.id}.</span> 
                {song.title}
              </div>
              <div className={`text-[11px] opacity-80 truncate ${selectedId === song.id ? 'text-indigo-100' : 'text-neutral-400'}`}>
                {song.style}
              </div>
            </button>
          ))}
        </nav>
      </aside>

      {/* Mobile Drawer Menu */}
      <AnimatePresence>
        {isMenuOpen && (
          <motion.div
            initial={{ opacity: 0, x: -100 }}
            animate={{ opacity: 1, x: 0 }}
            exit={{ opacity: 0, x: -100 }}
            className="fixed inset-0 bg-white z-[60] p-8 pt-24 md:hidden overflow-y-auto"
          >
            <nav className="space-y-4">
              {SONGS.map((song) => (
                <button
                  key={song.id}
                  onClick={() => {
                    setSelectedId(song.id);
                    setIsMenuOpen(false);
                  }}
                  id={`nav-mobile-${song.id}`}
                  className={`w-full text-left p-6 rounded-2xl border-2 transition-all ${
                    selectedId === song.id 
                    ? 'border-indigo-600 bg-indigo-50 text-indigo-900' 
                    : 'border-neutral-100 hover:border-indigo-200'
                  }`}
                >
                  <div className="text-2xl font-bold mb-1">
                    <span className="opacity-30 mr-3">{song.id}</span>
                    {song.title}
                  </div>
                  <div className="text-sm text-neutral-500 uppercase tracking-wider">{song.style}</div>
                </button>
              ))}
            </nav>
          </motion.div>
        )}
      </AnimatePresence>

      {/* Main Content Area */}
      <main className="md:ml-80 pt-20 md:pt-4 max-w-4xl mx-auto">
        <motion.div
          key={selectedId}
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.4 }}
          className="bg-white rounded-3xl p-8 md:p-16 shadow-sm border border-neutral-100"
          id="song-content"
        >
          <div className="mb-12 border-b border-neutral-100 pb-8">
            <div className="flex items-center gap-4 mb-6">
              <span className="text-4xl font-black text-indigo-200">{selectedSong.id}</span>
              <div className="h-0.5 flex-1 bg-indigo-50" />
              <div className="px-4 py-1.5 bg-indigo-50 text-indigo-600 rounded-full text-sm font-bold uppercase tracking-widest">
                {selectedSong.style}
              </div>
            </div>
            <h2 className="text-5xl md:text-8xl font-black text-neutral-900 leading-tight mb-4 tracking-tighter">
              {selectedSong.title}
            </h2>
          </div>

          <div 
            className="whitespace-pre-wrap text-2xl md:text-4xl text-neutral-700 leading-relaxed font-normal"
            style={{ fontSize: 'clamp(1.5rem, 4vw, 2.75rem)' }}
          >
            {renderLyrics(selectedSong.lyrics, selectedSong.id)}
          </div>

          <div className="mt-24 pt-12 border-t border-neutral-100 flex flex-col items-center justify-center text-neutral-200">
            <Music className="w-12 h-12 mb-4 opacity-50" />
            <p className="text-sm uppercase tracking-widest font-bold">Конец композиции</p>
          </div>
        </motion.div>
      </main>

      {/* Background Decor */}
      <div className="fixed top-0 right-0 -z-10 w-1/3 h-1/3 bg-indigo-50/50 rounded-full blur-3xl opacity-60" />
      <div className="fixed bottom-0 left-0 -z-10 w-1/4 h-1/4 bg-rose-50/40 rounded-full blur-3xl opacity-60" />
    </div>
  );
}
